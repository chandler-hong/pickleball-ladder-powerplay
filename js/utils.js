function pickRandomNames(count) {
  const fList = shuffle([...AMERICAN_FEMALE]);
  const mList = shuffle([...AMERICAN_MALE]);
  const cap = s => s.charAt(0).toUpperCase() + s.slice(1);

  // Random gender split: 25%-75% female, rest male
  const minF = Math.max(2, Math.round(count * 0.25));
  const maxF = Math.min(count - 2, Math.round(count * 0.75));
  const numF = minF + Math.floor(Math.random() * (maxF - minF + 1));
  const numM = count - numF;

  const result = [];
  const usedNames = new Set();
  let fi = 0, mi = 0;

  // Add females
  for (let i = 0; i < numF; i++) {
    while (fi < fList.length && usedNames.has(fList[fi])) fi++;
    const name = fi < fList.length ? fList[fi++] : `Player ${i + 1}`;
    usedNames.add(name);
    result.push({ name: cap(name), gender: 'F' });
  }
  // Add males
  for (let i = 0; i < numM; i++) {
    while (mi < mList.length && usedNames.has(mList[mi])) mi++;
    const name = mi < mList.length ? mList[mi++] : `Player ${numF + i + 1}`;
    usedNames.add(name);
    result.push({ name: cap(name), gender: 'M' });
  }

  return shuffle(result);
}
